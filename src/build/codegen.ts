/**
 * Code generation: produces an entry script for an extension.
 * One entry per extension — event/matcher dispatching happens via CLI args at runtime.
 */
import * as path from "node:path"
import { fileURLToPath } from "node:url"

/** Absolute path to src/runtime.ts within this package */
const RUNTIME_PATH = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..", "runtime.ts")

/**
 * Sanitize a matcher string for use in a filename.
 * Hex-encodes non-alphanumeric characters so the mapping is injective:
 * e.g. "Edit|Write" → "Edit_7cWrite", "foo*" → "foo_2a", "foo?" → "foo_3f"
 */
export function sanitizeMatcher(matcher: string): string {
	return matcher.replaceAll(/[^a-zA-Z0-9_-]/g, (ch) => `_${ch.codePointAt(0)!.toString(16)}`)
}

/**
 * Generate entry script source code for an extension.
 * Returns the code string — no file is written to disk.
 */
export function generateEntryCode(extensionEntryPoint: string): string {
	return `// Auto-generated by hx. Do not edit.
import { runHook } from ${JSON.stringify(RUNTIME_PATH)};
import extension from ${JSON.stringify(extensionEntryPoint)};
await runHook(extension);
`
}
