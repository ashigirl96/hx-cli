/**
 * Code generation: creates entry .ts files for each (event, matcher) pair.
 * These get bundled into standalone .mjs files by the bundler.
 */
import * as fs from "node:fs"
import * as path from "node:path"
import { fileURLToPath } from "node:url"

/** Absolute path to src/runtime.ts within this package */
const RUNTIME_PATH = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..", "runtime.ts")

export interface CodegenOptions {
	/** Absolute path to the extension entry file */
	extensionEntryPoint: string
	/** Hook event name */
	event: string
	/** Matcher pattern (undefined = no matcher) */
	matcher: string | undefined
	/** Output directory for generated entry files */
	outDir: string
}

/**
 * Sanitize a matcher string for use in a filename.
 * Hex-encodes non-alphanumeric characters so the mapping is injective:
 * e.g. "Edit|Write" → "Edit_7cWrite", "foo*" → "foo_2a", "foo?" → "foo_3f"
 */
function sanitizeMatcher(matcher: string): string {
	return matcher.replaceAll(/[^a-zA-Z0-9_-]/g, (ch) => `_${ch.codePointAt(0)!.toString(16)}`)
}

/**
 * Build the artifact filename stem for a (event, matcher) pair.
 * e.g. "PreToolUse__Bash" or "Stop" (no matcher).
 */
export function artifactName(event: string, matcher: string | undefined): string {
	if (matcher === undefined) {
		return event
	}
	return `${event}__${sanitizeMatcher(matcher)}`
}

/**
 * Generate an entry .ts file that imports the extension and calls runHook().
 * Returns the absolute path of the generated file.
 */
export function generateEntryScript(options: CodegenOptions): string {
	const { extensionEntryPoint, event, matcher, outDir } = options
	const name = artifactName(event, matcher)
	const entryPath = path.join(outDir, `${name}.entry.ts`)

	const matcherArg = matcher === undefined ? "undefined" : JSON.stringify(matcher)

	// Use absolute path for runtime — gets bundled away anyway
	const code = `// Auto-generated by hx. Do not edit.
import { runHook } from ${JSON.stringify(RUNTIME_PATH)};
import extension from ${JSON.stringify(extensionEntryPoint)};
await runHook(${JSON.stringify(event)}, ${matcherArg}, extension);
`

	fs.mkdirSync(path.dirname(entryPath), { recursive: true })
	fs.writeFileSync(entryPath, code, "utf-8")

	return entryPath
}
